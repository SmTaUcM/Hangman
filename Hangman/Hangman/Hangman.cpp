#include <iostream>
#include <fstream>
#include <string>
//-------------------------------------------------------------------------------------------------


//-------------------------------------------------------------------------------------------------
//                                         Main Program.                                             
//-------------------------------------------------------------------------------------------------
void read_text_file()
{
    std::string line;
    std::ifstream text_file ("word_list.txt");
    if (text_file.is_open())
    {
        while (std::getline(text_file, line))
        {
            std::cout << line << std::endl;
        }
        text_file.close();
    }

    else
    {
        std::cout << "Error - Unable to open file.";
    }

}
//-------------------------------------------------------------------------------------------------


void play_Hangman()
{
    read_text_file();
}
//-------------------------------------------------------------------------------------------------


int main()
{
    play_Hangman();
    return 0;
}
